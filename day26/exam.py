import re
#表达式
express='1 - 2 * ( (60-30 +(-40/5) * (9-2*5/3 + 7 /3*99/4*2998 +10 * 568/14 )) - (-4*3)/ (16-3*2) )'
#去空格
new_express=express.strip()
print(new_express)

#提取括号里边没有其他括号的表达式
ret01=re.search('\([^()]+\)',new_express)
print(ret01.group())

